public class goniometricke_funkce {
    public static void goniometricke_funkce(double cislo) {
        System.out.println("Sinus, cosinus, tangenc, cotangenc: ");
        System.out.println(Math.sin(cislo));
        System.out.println(Math.cos(cislo));
        System.out.println(Math.tan(cislo));
        System.out.println(1 / Math.tan(cislo) + "\n");


    }
}

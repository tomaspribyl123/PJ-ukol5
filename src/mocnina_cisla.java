public class mocnina_cisla {
    public static void mocnina_cisla(double cislo) {
        System.out.println("Na druhou, na třetí, 2. odmocnina, 3. odmocnina: ");
        System.out.println(Math.pow(cislo, 2));
        System.out.println(Math.pow(cislo, 3));
        System.out.println(Math.pow(cislo, (double) 1 / 2));
        System.out.println(Math.pow(cislo, (double) 1 / 3) + "\n");


    }
}
